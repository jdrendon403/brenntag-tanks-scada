import pandas as pd

# Factor de conversión exacto de Galón Americano a Litros
GAL_TO_L = 3.785411784

def export_tank_csv(tank_id, heights, volumes, extra_points=None):
    """
    Combina los puntos secuenciales y excepcionales de aforo, normaliza las 
    alturas a milímetros (mm) y convierte el volumen a Litros.
    """
    data = []
    # Incorporar puntos singulares del fondo o remates del cilindro si existen
    if extra_points:
        for hp, vp in extra_points:
            data.append((hp, vp))
            
    # Incorporar curvas de calibración principales
    for h, v in zip(heights, volumes):
        data.append((h, v))
        
    df = pd.DataFrame(data, columns=['height_cm', 'gal'])
    
    # Conversión técnica y limpieza de duplicados mecánicos
    df['height_mm'] = (df['height_cm'] * 10).round().astype(int)
    df['volume_l'] = (df['gal'] * GAL_TO_L).round(2)
    
    df = df.sort_values(by='height_mm').drop_duplicates(subset=['height_mm'])
    
    # Exportar al formato limpio requerido por el SCADA
    filename = f'tabla_aforo_tanque_{tank_id}.csv'
    df[['height_mm', 'volume_l']].to_csv(filename, index=False)
    print(f"Archivo exportado: {filename} con {len(df)} puntos de aforo calibrados.")

